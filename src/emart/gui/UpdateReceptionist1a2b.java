    private void loadReceptionistDetail() {
        try{
            receptionist=Receptionistdao.getAllReceptionistId();
            if(receptionist.isEmpty())
            {
                 JOptionPane.showMessageDialog(null,"No receptionist present.");
                 return;
            }
            Set <String> keys=receptionist.keySet();
            for(String i:keys)
            {
             id.addItem(i);
            }
        }
            catch(SQLException e)
       {
            JOptionPane.showMessageDialog(null,"Database Error");
            e.printStackTrace();
       }
        }
     private boolean validateInputs()
        {
            char []pwd=pass.getPassword();
            char []rpwd=rpass.getPassword();
            
        
        if(pwd.length < 4 || rpwd.length < 4 )
        {
           JOptionPane.showMessageDialog(null,"Password must be atleast of 4 characters");
           return false;
        }
       password=String.valueOf(pwd);
       String repassword=String.valueOf(rpwd);
       if(!password.equals(repassword))
       {
            JOptionPane.showMessageDialog(null,"Password not matching");
            return false;
       }
       return true;
    }
    private boolean isPasswordMatching(String a,String b)
    {
        return a.equals(b);
    }

    private void clearText() {
        
        pass.setText("");
        rpass.setText("");
    
    }
