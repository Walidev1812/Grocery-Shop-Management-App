    private void upActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_upActionPerformed

        if(!validateInputs())
        {
           return ;
        }
        try{

            String userid=id.getSelectedItem().toString();
            boolean result=Receptionistdao.updatePassword(userid,password);
            if(result)
            {
                 JOptionPane.showMessageDialog(null,"Password change successfully.");
                 clearText();
                 id.setSelectedItem(0);
                 return;
            }
              JOptionPane.showMessageDialog(null,"Password not change.");
              clearText();
            
            
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null,"Database Error");
            e.printStackTrace();
        }
    }//GEN-LAST:event_upActionPerformed

