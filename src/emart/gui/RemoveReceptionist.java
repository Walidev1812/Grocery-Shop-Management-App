    private void loadReceptionistId() {
         try{
            List<Receptionistpojo> empList=Receptionistdao.getAllReceptionistDetail();
            DefaultTableModel tm=(DefaultTableModel)tab.getModel();
            Object []rows=new Object[5];
            for(Receptionistpojo emp:empList)
            {
                rows[0]=emp.getEmpid();
                rows[1]=emp.getUserid();
                rows[2]=emp.getEmpname();
                rows[3]=emp.getJob();
                rows[4]=emp.getSalary();
                
                tm.addRow(rows);
            }
            
        }
         catch(SQLException e)
       {
            JOptionPane.showMessageDialog(null,"Database Error");
            e.printStackTrace();
       }
    }
