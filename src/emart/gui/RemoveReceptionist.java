    private void del1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_del1ActionPerformed
           int row = tab.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(null, "Please select record from the table.");
            return; // You should return here to avoid using 'row' later
        }

        tm = (DefaultTableModel) tab.getModel(); // Initialize 'tm' here

        String eid = tm.getValueAt(row, 0).toString();

        try {
            if (Receptionistdao.deleteReceptionist(eid)) {
                JOptionPane.showMessageDialog(null, "Employee deleted successfully..");
                total.setText("Number of Receptionist : "+(en-1));
                dele.setText("1 Receptionist remove");
                tm.setRowCount(0);
                loadReceptionistId();
                return;
            }
            JOptionPane.showMessageDialog(null, "Employee not deleted successfully..");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Database Error");
            e.printStackTrace();
        }


    }//GEN-LAST:event_del1ActionPerformed

